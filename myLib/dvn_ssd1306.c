//-----------------------------------API-------------------------------------
void DVN_ssd1306_I2C_Write(uint8_t address, uint8_t reg, uint8_t data) {
	uint8_t dt[2];
	dt[0] = reg;
	dt[1] = data;
	HAL_I2C_Master_Transmit(&hi2c1, address, dt, 2, 10);
}

void DVN_SSD1306_Fill(SSD1306_COLOR_t color) {
	memset(SSD1306_Buffer, (color == SSD1306_COLOR_BLACK) ? 0x00 : 0xFF, sizeof(SSD1306_Buffer));
}

void DVN_ssd1306_I2C_WriteMulti(uint8_t address, uint8_t reg, uint8_t* data, uint16_t count) {
	uint8_t dt[256];
	dt[0] = reg;
	uint8_t i = 0;
	for(i = 0; i < count; i++){
			dt[i+1] = data[i];
	}
	HAL_I2C_Master_Transmit(&hi2c1, address,dt, count+1, 10);
}

void DVN_SSD1306_UpdateScreen(void) {
	uint8_t m;

	for (m = 0; m < 8; m++) {
		DVN_SSD1306_WRITECOMMAND(0xB0 + m);
		DVN_SSD1306_WRITECOMMAND(0x00);
		DVN_SSD1306_WRITECOMMAND(0x10);
		/* Write multi data */
		DVN_ssd1306_I2C_WriteMulti(DVN_SSD1306_I2C_ADDR, 0x40, &SSD1306_Buffer[SSD1306_WIDTH * m], SSD1306_WIDTH);
	}
}



uint8_t DVN_SSD1306_Init(){
	uint32_t p = 250000;
	while(p>0)
		p--;
	//Device is not ready
	if (HAL_I2C_IsDeviceReady(&hi2c1, DVN_SSD1306_I2C_ADDR, 1, 20000) != HAL_OK) {
		return 0;
	}
	uint32_t p2 = 2500;
	while(p2>0)
		p2--;
	DVN_SSD1306_WRITECOMMAND(0xAE); 	//Display off
	DVN_SSD1306_WRITECOMMAND(0x20); 	//Set Memory Addressing Mode
	DVN_SSD1306_WRITECOMMAND(0x10); 	//00,Horizontal Addressing Mode;01,Vertical Addressing Mode;10,Page Addressing Mode (RESET);11,Invalid
	DVN_SSD1306_WRITECOMMAND(0xB0); 	//Set Page Start Address for Page Addressing Mode,0-7
	DVN_SSD1306_WRITECOMMAND(0xC8); 	//Set COM Output Scan Direction
	DVN_SSD1306_WRITECOMMAND(0x00); 	//---set low column address
	DVN_SSD1306_WRITECOMMAND(0x10); 	//---set high column address
	DVN_SSD1306_WRITECOMMAND(0x40); 	//--set start line address
	DVN_SSD1306_WRITECOMMAND(0x81); 	//--set contrast control register
	DVN_SSD1306_WRITECOMMAND(0xFF);
	DVN_SSD1306_WRITECOMMAND(0xA1); 	//--set segment re-map 0 to 127
	DVN_SSD1306_WRITECOMMAND(0xA6); 	//--set normal display
	DVN_SSD1306_WRITECOMMAND(0xA8); 	//--set multiplex ratio(1 to 64)
	DVN_SSD1306_WRITECOMMAND(0x3F); 	//
	DVN_SSD1306_WRITECOMMAND(0xA4); 	//0xa4,Output follows RAM content;0xa5,Output ignores RAM content
	DVN_SSD1306_WRITECOMMAND(0xD3); 	//-set display offset
	DVN_SSD1306_WRITECOMMAND(0x00); 	//-not offset
	DVN_SSD1306_WRITECOMMAND(0xD5); 	//--set display clock divide ratio/oscillator frequency
	DVN_SSD1306_WRITECOMMAND(0xF0); 	//--set divide ratio
	DVN_SSD1306_WRITECOMMAND(0xD9); 	//--set pre-charge period
	DVN_SSD1306_WRITECOMMAND(0x22); 	//
	DVN_SSD1306_WRITECOMMAND(0xDA); 	//--set com pins hardware configuration
	DVN_SSD1306_WRITECOMMAND(0x12);
	DVN_SSD1306_WRITECOMMAND(0xDB); 	//--set vcomh
	DVN_SSD1306_WRITECOMMAND(0x20); 	//0x20,0.77xVcc
	DVN_SSD1306_WRITECOMMAND(0x8D); 	//--set DC-DC enable
	DVN_SSD1306_WRITECOMMAND(0x14); 	//
	DVN_SSD1306_WRITECOMMAND(0xAF); 	//--turn on SSD1306 panel


	DVN_SSD1306_WRITECOMMAND(SSD1306_DEACTIVATE_SCROLL);
	
	DVN_SSD1306_Fill(SSD1306_COLOR_BLACK);
	
	DVN_SSD1306_UpdateScreen();
	
	//SSD1306.CurrentX = 0;
	//SSD1306.CurrentY = 0;
	
	SSD1306.Initialized = 1;
	
	return 1;
}

void DVN_SSD1306_GotoXY(uint16_t x, uint16_t y) {
	SSD1306.CurrentX = x;
	SSD1306.CurrentY = y;
}

void DVN_SSD1306_DrawPixel(uint16_t x, uint16_t y, SSD1306_COLOR_t color) {
	if ( x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT ) {
		return;
	}
	if (SSD1306.Inverted) {
		color = (SSD1306_COLOR_t)!color;
	}
	if (color == SSD1306_COLOR_WHITE) {
		SSD1306_Buffer[x + (y / 8) * SSD1306_WIDTH] |= 1 << (y % 8);
	} else {
		SSD1306_Buffer[x + (y / 8) * SSD1306_WIDTH] &= ~(1 << (y % 8));
	}
}

char DVN_SSD1306_Putc(char ch, DVN_FontDef_t* Font, SSD1306_COLOR_t color) {
	uint32_t i, b, j;
	if (
		SSD1306_WIDTH <= (SSD1306.CurrentX + Font->FontWidth) ||
		SSD1306_HEIGHT <= (SSD1306.CurrentY + Font->FontHeight)
	) {
		return 0;
	}
	for (i = 0; i < Font->FontHeight; i++) {
		b = Font->data[(ch - 32) * Font->FontHeight + i];
		for (j = 0; j < Font->FontWidth; j++) {
			if ((b << j) & 0x8000) {
				DVN_SSD1306_DrawPixel(SSD1306.CurrentX + j, (SSD1306.CurrentY + i), (SSD1306_COLOR_t) color);
			} else {
				DVN_SSD1306_DrawPixel(SSD1306.CurrentX + j, (SSD1306.CurrentY + i), (SSD1306_COLOR_t)!color);
			}
		}
	}

	SSD1306.CurrentX += Font->FontWidth;
	return ch;
}

char DVN_SSD1306_Puts(char* str, DVN_FontDef_t* Font, SSD1306_COLOR_t color) {
	while (*str) {
		if (DVN_SSD1306_Putc(*str, Font, color) != *str) {
			return *str;
		}
		str++;
	}
	return *str;
}